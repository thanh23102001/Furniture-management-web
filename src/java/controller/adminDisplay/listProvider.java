package controller.adminDisplay;

import controller.BaseRequiredAuthenticationController;
import dal.ProvidersDAO;
import java.io.IOException;
import java.io.PrintWriter;
import java.util.ArrayList;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import model.Providers;

/**
 *
 *  @author ADMIN
 */
public class listProvider extends BaseRequiredAuthenticationController {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.setCharacterEncoding("utf-8");
        String provider_name = request.getParameter("providers_name");
        System.out.println(provider_name);
        if (provider_name == null || provider_name.length() == 0) {
            provider_name = "";
        }
        try {
            ProvidersDAO pb = new ProvidersDAO();
            ArrayList<Providers> provider = pb.getProviderbyName(provider_name);
            request.setAttribute("provider", provider);
            request.setAttribute("provider_name", provider_name);
            request.getRequestDispatcher("../admin/display/providerView.jsp").forward(request, response);
        } catch (Exception ex) {
            request.getRequestDispatcher("../admin/newjsp.jsp").forward(request, response);
        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

    @Override
    protected void processGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        processRequest(request, response);
    }

    @Override
    protected void processPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        processRequest(request, response);
    }
}
