package model;

/**
 *
 *  @author ADMIN
 */
public class RoleAccount {

    int roleid;
    String rolename;

    public RoleAccount() {
    }

    public RoleAccount(int roleid, String rolename) {
        this.roleid = roleid;
        this.rolename = rolename;
    }

    public int getRoleid() {
        return roleid;
    }

    public void setRoleid(int roleid) {
        this.roleid = roleid;
    }

    public String getRolename() {
        return rolename;
    }

    public void setRolename(String rolename) {
        this.rolename = rolename;
    }

}
